#!/usr/bin/env python3
"""
    Esta función suma dos números enteros y devuelve el resultado.
    """
def add(a: float, b: float) -> float:
        """
    Suma dos números flotantes y devuelve el resultado.

    :param a: El primer número flotante
    :param b: El segundo número flotante
    :return: La suma de los dos números flotantes
    """
        return a + b
