#!/usr/bin/env python3
"""
Este módulo proporciona una función para obtener el floor de un número float.
"""

import math


def floor(n: float) -> int:
    """
    Devuelve el floor de un número flotante.

    :param n: El número flotante
    :return: El floor del número flotante como un entero
    """
    return math.floor(n)
