#!/usr/bin/env python3
"""
Proporciona una función para convertir un número flotante en cadena.
"""


def to_str(n: float) -> str:
    """
    Convierte un número flotante a su representación en cadena.

    :param n: El número flotante
    :return: La representación en cadena del número flotante
    """
    return str(n)
