#!/usr/bin/env python3
"""
Este módulo proporciona una función para concatenar cadenas.
"""


def concat(str1: str, str2: str) -> str:
    """
    Concatena dos cadenas y devuelve el resultado.

    :param str1: La primera cadena
    :param str2: La segunda cadena
    :return: La concatenación de las dos cadenas
    """
    return str1 + str2
